// ternary operatpor or conditional operators 

// The syntax of the ternary operator in JavaScript is as follows:
// condition ? expression1 : expression2

// The condition is evaluated, and if it is true, expression1 is executed. Otherwise, if the condition is false, expression2 is executed.

// let age = 20;
// let message = (age >= 18) ? "You are an adult" : "You are a minor";
// console.log(message);


// The ternary operator can also handle multiple conditions by nesting ternary operators. Here's an example:

// let num = 10;
// let message = (num > 0) ? "Positive" : (num < 0) ? "Negative" : "Zero";
// console.log(message);

